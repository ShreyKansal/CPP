#include <iostream> 
using namespace std;
int main () {
    int x,y;
    cout << "Enter first number";
    cin >> x;
    cout << "Enter second number";
    cin >> y;
    cout << x+y <<endl;    //Addition Operator
    cout << x-y <<endl;    //Subtraction Operator
    cout << x/y <<endl;    //Division Operator
    cout << x*y;           //Multiplication Operator
    return 0;
}

//The above program shows all the arithmetic operations in C++ programming.